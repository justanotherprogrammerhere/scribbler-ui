//Show SignInModal
function showSignInModal(){
  var modal = document.getElementById("signin-modal");
  modal.style.display = "flex";
}

//Hide SignInModal
function hideSignInModal(){
  var modal = document.getElementById("signin-modal");
  modal.style.display = "none";
}

